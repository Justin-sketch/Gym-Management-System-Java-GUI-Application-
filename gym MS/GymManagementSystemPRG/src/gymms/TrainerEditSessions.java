    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String action = actionCB.getSelectedItem().toString();
        String  sessionID = sessionT.getText();
        String sessionType = sTypeCB.getSelectedItem().toString();
        String  stringDateTime = dateTimeT.getText();
        String members = membersTA.getText();
        
        DateTimeFormatter formatter = DateTimeFormatter.ISO_LOCAL_DATE_TIME;
        LocalDateTime datetime = LocalDateTime.parse(stringDateTime, formatter );
        
        if (action.equals("Reschedule")){
            try{
                int a=JOptionPane.showConfirmDialog(null, "Do you really want to reschedule session.","Select", JOptionPane.YES_NO_OPTION);
                if (a == 0){
                Connection con = DBConnection.getCon();
                PreparedStatement ps = con.prepareStatement("update sessions set member_id=?, Session_type=?, date_and_time=? where member_id = ? and exercise = ?");
                ps.setString(1, members);
                ps.setString(2, sessionType);
                ps.setObject(3, datetime);           
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Session updated succesfully.");
                setVisible(false);
                new TrainerSessions().setVisible(true);
            }    
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }else{
            try{
                int a=JOptionPane.showConfirmDialog(null, "Do you really want to delete session","Select", JOptionPane.YES_NO_OPTION);
                if (a == 0){
                    Connection con = DBConnection.getCon();            
                    PreparedStatement ps = con.prepareStatement("delete from sessions where sessions_id =?");
                    ps.setString(1,sessionID);                
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Successfully deleted");
                    setVisible(false);
                    new AdminEditUserProfiles().setVisible(true);
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

