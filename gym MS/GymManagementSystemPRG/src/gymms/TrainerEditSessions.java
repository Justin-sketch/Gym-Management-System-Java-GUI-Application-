    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String sessionID = sessionT.getText();
        String trainer = SignIn.loggedInUsername;
        
        try{
            Connection con = DBConnection.getCon();
            PreparedStatement ps = con.prepareStatement("select * from sessions where trainer_id = ?");
            ps.setString(1,trainer);
            ResultSet rs = ps.executeQuery();
            if (rs.next()){                                       
                dateTimeT.setText(rs.getObject("date_and_time").toString());
                membersTA.setText(rs.getString("full_name"));                    
            }else{
                JOptionPane.showMessageDialog(null, "Session not found!");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
        
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

