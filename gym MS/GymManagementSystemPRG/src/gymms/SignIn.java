    private void SignInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignInActionPerformed
        // TODO add your handling code here:
        String username = signInU.getText();
        String password = new String(signInP.getPassword()).trim();
        String userRole = authenticateUser(username, password);
        
        if (username.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(null, " Please enter all fields!");
        }else{              
            switch(userRole){
                case "Admin":
                    JOptionPane.showMessageDialog(null,"Login successful!");
                    setVisible(false);
                    new AdminDashboard().setVisible(true);
                    
                    break;
                case "Member":
                    JOptionPane.showMessageDialog(null,"Login successful!");
                    setVisible(false);
                    new MemberDashboard().setVisible(true);
                    break;
                case "Trainer":
                    JOptionPane.showMessageDialog(null,"Login successful!");
                    setVisible(false);
                    new TrainerDashboard().setVisible(true);
                    break;
                case "IT":
                    JOptionPane.showMessageDialog(null,"Login successful!");
                    setVisible(false);
                    new ITDashboard().setVisible(true);
                    break;
                default:
                    JOptionPane.showMessageDialog(null,"Invalid login username or password.");                
                    break;
            }
        
        }
        
        
    }//GEN-LAST:event_SignInActionPerformed

