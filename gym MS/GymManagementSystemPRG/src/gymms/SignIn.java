    public static String authenticateUser(String username, String password){
        Connection con = DBConnection.getCon();
        try{
            PreparedStatement psAdmin = con.prepareStatement("select * from admins where admin_id = ? AND password = ?");
            PreparedStatement psMember = con.prepareStatement("select * from members where member_id = ? AND password = ?");
            PreparedStatement psTrainer = con.prepareStatement("select * from trainers where trainer_id = ? AND password = ?");
            PreparedStatement psIT = con.prepareStatement("select * from its where it_id = ? AND password = ?");
            
            
            psAdmin.setString(1, username);
            psAdmin.setString(2, password);
            
            try(ResultSet rs = psAdmin.executeQuery()){
                if(rs.next()){                    
                    loggedInUsername = username;
                    return "Admin";
                }    
            }
            
            psMember.setString(1, username);
            psMember.setString(2, password);
            
            try(ResultSet rs = psMember.executeQuery()){
                if(rs.next()){
                    loggedInUsername = username;
                    return "Member";
                }
            }
            
            psTrainer.setString(1, username);
            psTrainer.setString(2, password);
            
            try(ResultSet rs = psTrainer.executeQuery()){
                if(rs.next()){
                    loggedInUsername = username;
                    return "Trainer";
                }
            }
            
            psIT.setString(1, username);
            psIT.setString(2, password);
            
            try(ResultSet rs = psIT.executeQuery()){
                if(rs.next()){
                    loggedInUsername = username;
                    return "IT";
                }
            }
            
            
        }catch(SQLException e){
            e.printStackTrace();
        }
        return "invalid";
    } 
    
